/* Set the width of the side navigation to 250px */
function openNav() {
    document.getElementById("sideNav").style.width = "250px";
    document.getElementById("closebtn").style.display = "inherit";
    document.getElementById("homebtn-side").style.display = "inherit";
    document.getElementById("web-btn-side").style.display = "inherit";
    document.getElementById("contact-btn-side").style.display = "inherit";
    document.getElementById("about-btn-side").style.display = "inherit";
    document.getElementById("side-bar-logo").style.display = "inherit";
}
  
  /* Set the width of the side navigation to 0 */
function closeNav() {
    document.querySelector("#sideNav").style.width = "0";
    document.querySelector("#closebtn").style.display = "none";
    document.querySelector("#homebtn-side").style.display = "none";
    document.querySelector("#web-btn-side").style.display = "none";
    document.querySelector("#contact-btn-side").style.display = "none";
    document.querySelector("#about-btn-side").style.display = "none";
    document.querySelector("#side-bar-logo").style.display = "none";
}



